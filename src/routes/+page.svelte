	<h1>bvdget: not quite a budget</h1>

	<p>
		To know where you're going, you first need to know where you are. Most other budgeting apps or
		tools focus on planning out what you're going to spend in advance, and then it's up to you to
		try to hit those targets.
	</p>

	<p>
		<b>bvdget</b> takes a different approach, emphasizing tracking over planning. And not just for expenses.
		Income, savings, and debt are important components of your financial picture, so you can track those
		as well.
	</p>

	<p>
		By seeing the full picture, you'll have the information you need to use your best judgment for
		financial decisions. Can I justify this fun purchase? Am I spending more than I'm earning? Which
		debt should I prioritize paying off first?
	</p>

	<h2>Expenses</h2>

	<p>
		Spending money tends to happen more frequently than earning it, so that's the primary focus for
		tracking. It's also the category you have the most control over (though still not as much
		control as you'd probably like).
	</p>

	<p>
		For one-off purchases that you can't predict, the recommended approach is to track it as soon as
		you spend that money, lest you forget. Each expense gets tracked with the date of the purchase,
		a category and description that you define, and the amount that you spent. You can also leave
		the description blank if the category is descriptive enough as-is, like Groceries.
	</p>

	<p>
		For more predictable expenses, like rent/mortgage payments or subscriptions, you can set those
		up as recurring expenses. You track them with the same info as normal expenses, but you can
		specify how frequently they happen, like every month on the 15th or every year on March 4th. You
		can also toggle whether they're active or not, like if you have a subscription you're canceling
		for now but will probably reactivate later.
	</p>

	<h2>Income</h2>

	<p>
		Income works the same way as expenses, with the key difference being that you're tracking the
		money that's coming in, not going out. It probably makes sense to set up recurring income if you
		have a steady paycheck, but you should still check that the numbers match whenever you get paid
		or at the end of the month.
	</p>

	<p>
		If your income is less consistent, then tracking in real time is the way to go. This has the
		added benefit of not making it seem like you've already earned the money when looking at the big
		picture, which might inform your shopping decisions.
	</p>

	<h2>Savings</h2>

	<p>
		Since savings aren't transactional, the approach for tracking them is to take monthly snapshots.
		So at the beginning of the month, you can take a look at each place where you have money saved,
		like checking/savings accounts, retirement accounts, cash, and so on. For each one, you'll give
		it a category and an optional description and track the balance.
	</p>

	<p>
		In the short term, these snapshots give you a sense of where you are financially. Over time,
		you'll be able to see your progress and see what the trend line looks like. Are you moving
		forward or backward? How much did you have saved last year compared to now? What can you do to
		make that number go up?
	</p>

	<p>
		Since the categories aren't likely to change but the numbers are, you can copy last month's
		savings at the beginning of the month and then edit them all at once.
	</p>

	<h2>Debt</h2>

	<p>
		Similar to savings, debt is tracked with the same monthly snapshot approach. However, you will
		also track the minimum payment and the interest rate for each source of debt, like loans or
		credit card debt.
	</p>

	<p>
		This extra info is useful for prioritization. You could eventually pay off everything by paying
		the minimum payment for each debt, but that's a slow and expensive process. If you have extra
		money, you should be throwing it at the debt with the highest interest rate. If you can't afford
		even the minimum payments, you should still prioritize the highest interest rate debt first
		(unless there are massive fees or other mitigating factors).
	</p>

	<p>
		Over time, you will be able to see your progress. Debt is notoriously sticky and hard to get rid
		of, but the snapshots should help you see whether it's going down at a healthy rate.
	</p>

	<h2>Putting it all together</h2>

	<p>
		Tracking this data is useful in both the short term and the long term. In the short term, you
		can see where you're at financially, helping you make decisions in the moment. In the long term,
		you can see where you've been and predict where you're going, helping you make those bigger
		decisions, like knowing how much you'll need to have saved to be able to retire.
	</p>

	<p>
		The data really starts to get useful once you have three or more months tracked, so if you want
		to get started by going through statements or receipts, you can, or you can start from scratch
		and stick with it for a while. This isn't a paid app, and there aren't any paywalled features,
		so even if you just want to kick the tires, that's fine too.
	</p>

